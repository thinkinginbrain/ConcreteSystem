package com.wzc.login.domain;

public class Limit {
	private Integer id;
	
	private String name;
	
	private String lim;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String username) {
		this.name = username;
	}

	public String getLimit() {
		return lim;
	}

	public void setLimit(String limit) {
		this.lim = limit;
	}
	
    
	

}
